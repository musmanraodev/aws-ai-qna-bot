var Vuex=require('vuex')
var saveAs=require('file-saver').saveAs
var Promise=require('bluebird')
var _=require('lodash')
var empty=require('./empty')

module.exports={
  props:['data','label'],
  data:function(){
    return {
      error:'',
      success:'',
      dialog:false,
      loading:false,
      opened:false,
      valid:true,
      tmp:{}
    }
  },
  components:{
    "schema-input":require('./input.vue')
  },
  computed:{
    schema:function(){
      return this.$store.state.data.schema
    }
  },
  methods:{
    cancel:function(){
      this.dialog=false
      this.loading=false
    },
    refresh:function(){
      if(!this.opened){
        this.tmp=_.merge(empty(this.schema),_.cloneDeep(this.data))
        this.opened=true
      }
    },
    update:function(){
      var self=this
      if(this.valid){
        self.loading=true
        self.dialog=false

        return Promise.resolve((function(){
          if(self.data.qid!==self.tmp.qid){
            return self.$store.dispatch('api/check',self.tmp.qid)
            .then(x=> x ? Promise.reject("Question with that ID already Exists") : null )
            .then(()=>self.$store.dispatch('api/remove',self.data.qid))
          }
        })())
        .then(function(){
          return self.$store.dispatch('data/update',self.tmp)
          .then(function(result){
            self.$emit('update:data',_.cloneDeep(self.tmp))
            self.success="!success"
          })
        })
        .catch(function(error){
            self.dialog=true
            self.loading=false
            console.log(error)
            self.error=error
        })
      }
    }
  }
}
</script>

